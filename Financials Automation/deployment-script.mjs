import { spawn } from 'child_process';
import { fileURLToPath } from 'url';
import { dirname, join } from 'path';
import fs from 'fs';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);

function loadEnvFile() {
  const envPath = join(__dirname, '.env');
  
  if (!fs.existsSync(envPath)) {
    return;
  }
  
  const envContent = fs.readFileSync(envPath, 'utf8');
  const envLines = envContent.split('\n');
  
  for (const line of envLines) {
    const trimmedLine = line.trim();
    if (trimmedLine && !trimmedLine.startsWith('#')) {
      const [key, ...valueParts] = trimmedLine.split('=');
      const value = valueParts.join('=');
      if (key && value) {
        process.env[key] = value;
      }
    }
  }
}

function maskSensitiveValue(value) {
  if (!value) return 'NOT SET';
  if (value.length <= 8) return '***';
  return value.substring(0, 4) + '***' + value.substring(value.length - 4);
}

async function runDiagnosticPhase() {
  console.log('üîç Running system diagnostics...');
  console.log('');
  
  try {
    const diagnosticResult = await new Promise((resolve, reject) => {
      const diagnosticProcess = spawn('node', ['diagnostic-test.mjs'], {
        stdio: 'inherit',
        env: process.env,
        cwd: __dirname
      });
      
      diagnosticProcess.on('close', (code) => {
        resolve(code === 0);
      });
      
      diagnosticProcess.on('error', (error) => {
        console.error('‚ùå Failed to run diagnostic test:', error);
        reject(error);
      });
    });
    
    return diagnosticResult;
  } catch (error) {
    console.error('‚ùå Diagnostic phase failed:', error);
    return false;
  }
}

